    Instrument(int id, String label) {
        this(id, label, false);
    }

    Instrument(int id, String label, boolean drumKit) {
        this.id = id;
        this.label = label;
        this.drumKit = drumKit;
    }

    public int id() {
        return id;
    }

    public String label() {
        return label;
    }

    public boolean isDrumKit() {
        return drumKit;
    }

    public int midi() {
        return drumKit ?
                Math.abs(id) - 1 :
                id;
    }

    public static Instrument of(final int id) {
        for(Instrument instrument : values())
            if (instrument.id == id)
                return instrument;

        throw new IllegalArgumentException("Unknown instrument with id=" + id);
    }

    public static Instrument of(final String name) {
        for(Instrument instrument : values())
            if (instrument.label.equals(name))
                return instrument;

        throw new IllegalArgumentException("Unknown instrument with name=" + name);
    }
